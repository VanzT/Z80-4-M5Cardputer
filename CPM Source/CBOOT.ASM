;------------------------------------------------------------------------------
;
; Z80 Monitor Rom
;
;------------------------------------------------------------------------------
; General Equates
;------------------------------------------------------------------------------

CR		.EQU	0DH
LF		.EQU	0AH
ESC		.EQU	1BH
CTRLC		.EQU	03H
CLS		.EQU	0CH

;Virtual I/O Registers
uart_a_tx	equ	$ff00
uart_a_rx	equ	$ff10
uart_b_tx	equ	$ff01
uart_b_rx	equ	$ff11
dparm		equ	$ff30
dcmd		equ	$ff20

STACK   	.EQU    $4032	; Stack top



;------------------------------------------------------------------------------
;                         START OF MONITOR ROM
;------------------------------------------------------------------------------

		.org	$0000


		JP	$E600
		;JP	$FFFE

		.END
